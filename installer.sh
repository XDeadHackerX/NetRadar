check_wifi_mode() {
	#Gurdar en el contenedor $modo si la tarjeta esta en modo Monitor o Managed
  mode_check=$(sudo iwconfig $1 | grep Mode | awk '{print $1}')
  if [ "$mode_check" == "Mode:Monitor" ]; then
    mode=Monitor
  else
    mode=Managed
  fi
}

